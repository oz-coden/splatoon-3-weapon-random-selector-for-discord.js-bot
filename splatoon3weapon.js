const baseCategories = [
  "shooter_mode",
  "roller_mode",
  "charger_mode",
  "bucket_mode",
  "spiner_mode",
  "dualies_mode",
  "shelter_mode",
  "blaster_mode",
  "brush_mode",
  "bow_mode",
  "wiper_mode",
];
const allWeapons = baseCategories.flatMap(
  (mode) => weaponCategories[mode].weapons,
);

function getRandomElement(array) {
  return array[Math.floor(Math.random() * array.length)];
}

module.exports.getRandomWeaponResult = function (mode) {
  if (!mode || mode === "normal_mode") {
    const weapon = getRandomElement(allWeapons);
    return `選ばれたのは、「${weapon}」でした！`;
  }

  const category = weaponCategories[mode];
  if (category) {
    const weapon = getRandomElement(category.weapons);
    return `選ばれたのは、「${weapon}」でした！（${category.label}）`;
  }

  return `選ばれたのは、「${getRandomElement(allWeapons)}」でした！`;
};
